public class Task7 {
    public static void main(String[] args) {
        int result = 70;
        result += 10;
        System.out.println("10 - ის დამატება " + result);

        result -= 5;
        System.out.println("5-ის გამოკლება " + result);

        result *= 2;
        System.out.println("2-ზე გამრავლების შემდეგ: " + result);

        result /= 3;
        System.out.println("3-ზე გაყოფის შემდეგ: " + result);
    }
}
